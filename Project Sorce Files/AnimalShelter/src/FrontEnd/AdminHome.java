        {
            curr++;
            pageno3.setText(curr+"/"+total);
            
            if (ed.getText().length() > 3)
            {
                fill5(result,true);
            }
            else if (sd.getText().length() > 3)
            {
                fill4(result,true);
            }
            else
            {
            fill3(result,true);
            
            }

        }
        
