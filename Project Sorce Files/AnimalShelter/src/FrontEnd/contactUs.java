    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
       
        int errors = 0;
        String loginn = login.getText();
        String password = passwrd.getText();
        
         if (login.getText().length() < 1 || login.getText().equals("usman") == false)
        {
            if (login.getText().length()<1)
              {
                  login.setText("please enter a login");
              }
            errors += 1;
            login.setForeground(new java.awt.Color(255, 0, 0));
        }
         
        if (passwrd.getText().length() < 1 || passwrd.getText().equals("usman") == false)
        {
            if (passwrd.getText().length()<1)
              {
                  passwrd.setText("please enter a password");
              }
            errors += 1;
            passwrd.setForeground(new java.awt.Color(255, 0, 0));
        }
        
        if (errors == 0)
        {
            new AdminHome().setVisible(true);
            this.setVisible(false);
            
        }
        
        
        
    }//GEN-LAST:event_jButton12ActionPerformed

